package goast

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"sort"
	"strings"

	"golang.org/x/tools/imports"
)

type DeclSource struct {
	File   string
	Line   int
	Column int
}

// SourceFile represents a Go source file being built.
// It provides a similar interface to jennifer's *jen.File.
type SourceFile struct {
	pkgName     string
	header      string
	imports     map[string]string // path -> alias (empty alias means default)
	importOrder []string
	decls       []ast.Decl
	declSources []DeclSource
}

// NewSourceFile creates a new SourceFile with the given package name.
func NewSourceFile(pkgName string) *SourceFile {
	return &SourceFile{
		pkgName:     pkgName,
		header:      "Code generated by mygo; DO NOT EDIT.",
		imports:     map[string]string{},
		importOrder: nil,
	}
}

// HeaderComment sets the header comment for the file.
func (f *SourceFile) HeaderComment(comment string) {
	f.header = comment
}

// AddImport registers an import. Use empty alias for default import name.
// For dot-imports, set alias to ".".
func (f *SourceFile) AddImport(path, alias string) {
	if _, ok := f.imports[path]; ok {
		return
	}
	f.imports[path] = alias
	f.importOrder = append(f.importOrder, path)
}

// AddDecl adds a top-level declaration to the file.
func (f *SourceFile) AddDecl(decl ast.Decl) {
	f.decls = append(f.decls, decl)
	f.declSources = append(f.declSources, DeclSource{})
}

// AddDeclWithSource adds a top-level declaration and records its MyGO source
// location for later diagnostics.
func (f *SourceFile) AddDeclWithSource(decl ast.Decl, src DeclSource) {
	f.decls = append(f.decls, decl)
	f.declSources = append(f.declSources, src)
}

// AddDecls adds multiple top-level declarations.
func (f *SourceFile) AddDecls(decls []ast.Decl) {
	for _, decl := range decls {
		f.AddDecl(decl)
	}
}

// AddDeclsWithSource adds multiple declarations sharing the same MyGO source
// location.
func (f *SourceFile) AddDeclsWithSource(decls []ast.Decl, src DeclSource) {
	for _, decl := range decls {
		f.AddDeclWithSource(decl, src)
	}
}

// ImportSpecs returns the sorted import specs for the file.
func (f *SourceFile) ImportSpecs() []*ast.ImportSpec {
	sorted := sort.StringSlice(f.importOrder)
	sorted.Sort()
	specs := make([]*ast.ImportSpec, 0, len(sorted))
	for _, path := range sorted {
		alias := f.imports[path]
		spec := ImportSpec(path, alias)
		specs = append(specs, spec)
	}
	return specs
}

// Render produces the formatted Go source as a string.
func (f *SourceFile) Render() (string, error) {
	file := f.RenderAST()
	if err := validateAST(file, f.renderDeclSources()); err != nil {
		return "", err
	}

	// Render with go/format.
	var buf bytes.Buffer
	defer func() {
		if r := recover(); r != nil {
			panic(fmt.Errorf("go/format panic: %v", r))
		}
	}()
	if err := format.Node(&buf, token.NewFileSet(), file); err != nil {
		return "", fmt.Errorf("go/format: %w", err)
	}
	src := buf.String()
	if f.header != "" {
		src = "// " + f.header + "\n\n" + src
	}
	if out, ok := runGoimports(src); ok {
		return out, nil
	}
	return src, nil
}

func (f *SourceFile) renderDeclSources() []DeclSource {
	sources := make([]DeclSource, 0, len(f.declSources)+1)
	if len(f.imports) > 0 {
		sources = append(sources, DeclSource{})
	}
	sources = append(sources, f.declSources...)
	return sources
}

func runGoimports(src string) (string, bool) {
	out, err := imports.Process("generated.go", []byte(src), nil)
	if err != nil {
		return "", false
	}
	return string(out), true
}

func validateAST(file *ast.File, declSources []DeclSource) error {
	var walkExpr func(path string, e ast.Expr) error
	var walkStmt func(path string, s ast.Stmt) error
	var walkDecl func(path string, d ast.Decl) error

	walkExpr = func(path string, e ast.Expr) error {
		if e == nil {
			return fmt.Errorf("nil ast.Expr at %s", path)
		}
		switch v := e.(type) {
		case *ast.CallExpr:
			if v.Fun == nil {
				return fmt.Errorf("nil call fun at %s", path)
			}
			if err := walkExpr(path+".Fun", v.Fun); err != nil {
				return err
			}
			for i, a := range v.Args {
				if err := walkExpr(fmt.Sprintf("%s.Args[%d]", path, i), a); err != nil {
					return err
				}
			}
		case *ast.CompositeLit:
			// Allow anonymous struct literals (Type == nil) when the element type
			// can be inferred from context (e.g., []CompletionItem{...}).
			if v.Type == nil {
				// Skip validation for anonymous struct literals.
				for i, a := range v.Elts {
					if err := walkExpr(fmt.Sprintf("%s.Elts[%d]", path, i), a); err != nil {
						return err
					}
				}
				return nil
			}
			if err := walkExpr(path+".Type", v.Type); err != nil {
				return err
			}
			for i, a := range v.Elts {
				if err := walkExpr(fmt.Sprintf("%s.Elts[%d]", path, i), a); err != nil {
					return err
				}
			}
		case *ast.KeyValueExpr:
			if err := walkExpr(path+".Key", v.Key); err != nil {
				return err
			}
			if err := walkExpr(path+".Value", v.Value); err != nil {
				return err
			}
		case *ast.FuncLit:
			if v.Type == nil {
				return fmt.Errorf("nil func type at %s", path)
			}
			if err := walkExpr(path+".Type", v.Type); err != nil {
				return err
			}
			if v.Body != nil {
				if err := walkStmt(path+".Body", v.Body); err != nil {
					return err
				}
			}
		case *ast.IndexExpr:
			if err := walkExpr(path+".X", v.X); err != nil {
				return err
			}
			if err := walkExpr(path+".Index", v.Index); err != nil {
				return err
			}
		case *ast.IndexListExpr:
			if err := walkExpr(path+".X", v.X); err != nil {
				return err
			}
			for i, idx := range v.Indices {
				if err := walkExpr(fmt.Sprintf("%s.Indices[%d]", path, i), idx); err != nil {
					return err
				}
			}
		case *ast.ParenExpr:
			return walkExpr(path+".X", v.X)
		case *ast.SelectorExpr:
			return walkExpr(path+".X", v.X)
		case *ast.StarExpr:
			return walkExpr(path+".X", v.X)
		case *ast.UnaryExpr:
			return walkExpr(path+".X", v.X)
		case *ast.BinaryExpr:
			if err := walkExpr(path+".X", v.X); err != nil {
				return err
			}
			return walkExpr(path+".Y", v.Y)
		case *ast.ArrayType:
			if v.Len != nil {
				if err := walkExpr(path+".Len", v.Len); err != nil {
					return err
				}
			}
			return walkExpr(path+".Elt", v.Elt)
		case *ast.MapType:
			if err := walkExpr(path+".Key", v.Key); err != nil {
				return err
			}
			return walkExpr(path+".Value", v.Value)
		case *ast.StructType:
			if v.Fields != nil {
				for i, fld := range v.Fields.List {
					for j, name := range fld.Names {
						_ = j
						if name == nil {
							return fmt.Errorf("nil field name at %s.Fields[%d]", path, i)
						}
					}
					if fld.Type == nil {
						return fmt.Errorf("nil field type at %s.Fields[%d]", path, i)
					}
					if err := walkExpr(fmt.Sprintf("%s.Fields[%d].Type", path, i), fld.Type); err != nil {
						return err
					}
				}
			}
		case *ast.FuncType:
			if v.Params != nil {
				for i, fld := range v.Params.List {
					if fld.Type == nil {
						return fmt.Errorf("nil func param type at %s.Params[%d]", path, i)
					}
					if err := walkExpr(fmt.Sprintf("%s.Params[%d].Type", path, i), fld.Type); err != nil {
						return err
					}
				}
			}
			if v.Results != nil {
				for i, fld := range v.Results.List {
					if fld.Type == nil {
						return fmt.Errorf("nil func result type at %s.Results[%d]", path, i)
					}
					if err := walkExpr(fmt.Sprintf("%s.Results[%d].Type", path, i), fld.Type); err != nil {
						return err
					}
				}
			}
		}
		return nil
	}

	walkStmt = func(path string, s ast.Stmt) error {
		if s == nil {
			return fmt.Errorf("nil ast.Stmt at %s", path)
		}
		switch v := s.(type) {
		case *ast.BlockStmt:
			for i, stmt := range v.List {
				if err := walkStmt(fmt.Sprintf("%s.List[%d]", path, i), stmt); err != nil {
					return err
				}
			}
		case *ast.ExprStmt:
			return walkExpr(path+".X", v.X)
		case *ast.AssignStmt:
			for i, e := range v.Lhs {
				if err := walkExpr(fmt.Sprintf("%s.Lhs[%d]", path, i), e); err != nil {
					return err
				}
			}
			for i, e := range v.Rhs {
				if err := walkExpr(fmt.Sprintf("%s.Rhs[%d]", path, i), e); err != nil {
					return err
				}
			}
		case *ast.ReturnStmt:
			for i, e := range v.Results {
				if err := walkExpr(fmt.Sprintf("%s.Results[%d]", path, i), e); err != nil {
					return err
				}
			}
		case *ast.DeclStmt:
			return walkDecl(path+".Decl", v.Decl)
		case *ast.IfStmt:
			if err := walkExpr(path+".Cond", v.Cond); err != nil {
				return err
			}
			if v.Body != nil {
				if err := walkStmt(path+".Body", v.Body); err != nil {
					return err
				}
			}
			if v.Else != nil {
				if err := walkStmt(path+".Else", v.Else); err != nil {
					return err
				}
			}
		}
		return nil
	}

	walkDecl = func(path string, d ast.Decl) error {
		if d == nil {
			return fmt.Errorf("nil ast.Decl at %s", path)
		}
		switch v := d.(type) {
		case *ast.FuncDecl:
			if v.Type != nil && v.Type.Params != nil {
				for i, fld := range v.Type.Params.List {
					if fld.Type == nil {
						return fmt.Errorf("nil func decl param type at %s.Type.Params[%d]", path, i)
					}
					if err := walkExpr(fmt.Sprintf("%s.Type.Params[%d].Type", path, i), fld.Type); err != nil {
						return err
					}
				}
			}
			if v.Type != nil && v.Type.Results != nil {
				for i, fld := range v.Type.Results.List {
					if fld.Type == nil {
						return fmt.Errorf("nil func decl result type at %s.Type.Results[%d]", path, i)
					}
					if err := walkExpr(fmt.Sprintf("%s.Type.Results[%d].Type", path, i), fld.Type); err != nil {
						return err
					}
				}
			}
			if v.Body != nil {
				return walkStmt(path+".Body", v.Body)
			}
		case *ast.GenDecl:
			for i, spec := range v.Specs {
				switch s := spec.(type) {
				case *ast.TypeSpec:
					if s.Type != nil {
						if err := walkExpr(fmt.Sprintf("%s.Specs[%d].Type", path, i), s.Type); err != nil {
							return err
						}
					}
				case *ast.ValueSpec:
					for j, e := range s.Values {
						if err := walkExpr(fmt.Sprintf("%s.Specs[%d].Values[%d]", path, i, j), e); err != nil {
							return err
						}
					}
					if s.Type != nil {
						if err := walkExpr(fmt.Sprintf("%s.Specs[%d].Type", path, i), s.Type); err != nil {
							return err
						}
					}
				}
			}
		}
		return nil
	}

	for i, decl := range file.Decls {
		if err := walkDecl(fmt.Sprintf("Decls[%d]", i), decl); err != nil {
			if name := declName(decl); name != "" {
				err = fmt.Errorf("%s: %w", name, err)
			}
			if i < len(declSources) {
				if msg := formatDeclSourceError(declSources[i], err); msg != "" {
					return fmt.Errorf("%s", msg)
				}
			}
			return err
		}
	}
	return nil
}

func declName(decl ast.Decl) string {
	switch d := decl.(type) {
	case *ast.FuncDecl:
		if d.Name != nil {
			return d.Name.Name
		}
	case *ast.GenDecl:
		if len(d.Specs) == 0 {
			return ""
		}
		switch s := d.Specs[0].(type) {
		case *ast.TypeSpec:
			if s.Name != nil {
				return s.Name.Name
			}
		case *ast.ValueSpec:
			if len(s.Names) > 0 && s.Names[0] != nil {
				return s.Names[0].Name
			}
		}
	}
	return ""
}

func formatDeclSourceError(src DeclSource, err error) string {
	if err == nil || src.File == "" {
		return ""
	}
	switch {
	case src.Line > 0 && src.Column > 0:
		return fmt.Sprintf("%s: line %d, col %d: %v", src.File, src.Line, src.Column, err)
	case src.Line > 0:
		return fmt.Sprintf("%s: line %d: %v", src.File, src.Line, err)
	default:
		return fmt.Sprintf("%s: %v", src.File, err)
	}
}

func importSpecLocalName(imp *ast.ImportSpec) string {
	if imp.Name != nil && imp.Name.Name != "" {
		return imp.Name.Name
	}
	path := strings.Trim(imp.Path.Value, `"`)
	if path == "unicode/utf8" {
		return "utf8"
	}
	for i := len(path) - 1; i >= 0; i-- {
		if path[i] == '/' {
			return path[i+1:]
		}
	}
	return path
}

func importSpecPackageName(path string) string {
	if path == "github.com/mygo-lang/mygo/prelude" {
		return "prelude"
	}
	if path == "unicode/utf8" {
		return "utf8"
	}
	for i := len(path) - 1; i >= 0; i-- {
		if path[i] == '/' {
			return path[i+1:]
		}
	}
	return path
}

// MustRender renders the source and panics on error (for testing).
func (f *SourceFile) MustRender() string {
	s, err := f.Render()
	if err != nil {
		panic(err)
	}
	return s
}

// RenderAST renders the AST file and returns the raw *ast.File.
// Useful for testing or further AST manipulation.
func (f *SourceFile) RenderAST() *ast.File {
	decls := make([]ast.Decl, 0, len(f.decls)+1)
	if len(f.imports) > 0 {
		importDecl := &ast.GenDecl{
			Tok:   token.IMPORT,
			Specs: make([]ast.Spec, 0, len(f.importOrder)),
		}
		sorted := sort.StringSlice(f.importOrder)
		sorted.Sort()
		for _, path := range sorted {
			alias := f.imports[path]
			spec := ImportSpec(path, alias)
			importDecl.Specs = append(importDecl.Specs, spec)
		}
		decls = append(decls, importDecl)
	}
	decls = append(decls, f.decls...)
	file := &ast.File{
		Name:  ast.NewIdent(f.pkgName),
		Decls: decls,
	}
	return file
}

// RenderNode renders any AST node as a string (useful for testing).
func RenderNode(node ast.Node) (string, error) {
	var buf bytes.Buffer
	if err := format.Node(&buf, token.NewFileSet(), node); err != nil {
		return "", err
	}
	return buf.String(), nil
}

// ParseCheck checks if the given source is valid Go.
// Returns nil if valid, or the parser error.
func ParseCheck(src string) error {
	_, err := parser.ParseFile(token.NewFileSet(), "", src, 0)
	return err
}
